package com.example.qunlsv.DoiTuong;

public class Nhom4 {
    private String mTen;
    private String mLop;
    private String mMSSV;
    private int mHinh;

    public Nhom4(String mTen, String mLop, String mMSSV, int mHinh) {
        this.mTen = mTen;
        this.mLop = mLop;
        this.mMSSV = mMSSV;
        this.mHinh = mHinh;
    }

    public String getmTen() {
        return mTen;
    }

    public void setmTen(String mTen) {
        this.mTen = mTen;
    }

    public String getmLop() {
        return mLop;
    }

    public void setmLop(String mLop) {
        this.mLop = mLop;
    }

    public String getmMSSV() {
        return mMSSV;
    }

    public void setmMSSV(String mMSSV) {
        this.mMSSV = mMSSV;
    }

    public int getmHinh() {
        return mHinh;
    }

    public void setmHinh(int mHinh) {
        this.mHinh = mHinh;
    }
}
